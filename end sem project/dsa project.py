import sys
INF = 999

def mindistance(distance, stat):
    minimum = sys.maxsize
    ind = -1
    for k in range(33):
        if not stat[k] and distance[k] <= minimum:
            minimum = distance[k]
            ind = k
    return ind

def dijkstra(graph, source, stations, destination):
    distance = [INF] * 33
    stat = [False] * 33
    parent = [-1] * 33

    distance[source] = 0

    for i in range(33):
        m = mindistance(distance, stat)
        stat[m] = True
        for k in range(33):
            if (not stat[k]) and graph[m][k] and distance[m] != INF and distance[m] + graph[m][k] < distance[k]:
                parent[k] = m
                distance[k] = distance[m] + graph[m][k]

    print(f"Minimum Number of Stations from {stations[source]} to {stations[destination]}: {distance[destination]}")

    
    print("Shortest Path: ", end="")
    curr = destination
    while curr != source:
        print(stations[curr] + " -> ", end="")
        curr = parent[curr]
    print(stations[source])

